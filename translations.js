function getTranslation(lang, key) {
  var v = translations[lang] && translations[lang][key];
  if (v === undefined || v === null) v = translations['en'] && translations['en'][key];
  return (v === undefined) ? null : v;
}

var RF_SUPPORTED_LANGS = ['en', 'ru', 'es', 'tr', 'pt', 'fr', 'de'];

function setLanguage(lang) {
  if (RF_SUPPORTED_LANGS.indexOf(lang) === -1) lang = 'en';
  try { localStorage.setItem('renumbLanguage', lang); } catch (e) {}
  /* Keep the choice in the URL so it matches the hreflang cluster and is
     shareable. 'en' is the x-default — drop the param for a clean URL. */
  try {
    var url = new URL(window.location.href);
    if (lang === 'en') url.searchParams.delete('lang');
    else url.searchParams.set('lang', lang);
    window.history.replaceState({}, '', url.toString());
    window.location.reload();
  } catch (e) {
    window.location.reload();
  }
}

function getCurrentLanguage() {
  /* Priority: ?lang= query param (drives hreflang) > saved choice > English. */
  try {
    var param = new URLSearchParams(window.location.search).get('lang');
    if (param && RF_SUPPORTED_LANGS.indexOf(param) !== -1) return param;
  } catch (e) {}
  try {
    var saved = localStorage.getItem('renumbLanguage');
    if (saved && RF_SUPPORTED_LANGS.indexOf(saved) !== -1) return saved;
  } catch (e) {}
  return 'en';
}

function updatePageText(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    var t = getTranslation(lang, el.dataset.i18n);
    if (t !== null) el.textContent = t;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    var t = getTranslation(lang, el.dataset.i18nPlaceholder);
    if (t !== null) el.setAttribute('placeholder', t);
  });
  document.querySelectorAll('[data-i18n-content]').forEach(el => {
    var t = getTranslation(lang, el.dataset.i18nContent);
    if (t !== null) el.setAttribute('content', t);
  });
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    var t = getTranslation(lang, el.dataset.i18nAria);
    if (t !== null) el.setAttribute('aria-label', t);
  });
  document.documentElement.setAttribute('lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLanguage();
  updatePageText(lang);
  const sel = document.querySelector('[data-lang-select]');
  if(sel) sel.value = lang;
});

